using namespace std;

unsigned short tamanhoJanelaX = 500; //px
unsigned short tamanhoJanelaY = tamanhoJanelaX; //px; //px
unsigned char tamanhoPixel = tamanhoJanelaX*(tamanhoJanelaX/tamanhoJanelaY)/72;
int offset = 0;


class Ponto{
	private:
		short x,y;
		unsigned char r;
		unsigned char g;
		unsigned char b;

	public:
		Ponto(short x, short y, unsigned char r,unsigned char g,unsigned char b){
			this->x = x;
			this->y = y;
			this->r = r;
			this->g = g;
			this->b = b;
		}

		void set_posicao(short x, short y){
			this->x = x;
			this->y = y;
		}

		void set_cor(unsigned char r,unsigned char g,unsigned char b){
			this->r = r;
			this->g = g;
			this->b = b;
		}

		void imprimir_pixel(){
			glColor3f((double)r/255, (double)g/255, (double)b/255); // Red
			//glVertex2d((double)((x*tamanhoPixel*2))/tamanhoJanelaX-offsetX, (double)-(y*tamanhoPixel*2)/tamanhoJanelaY+offsetY);
			glVertex2i(x*tamanhoPixel+tamanhoPixel/2+5*tamanhoPixel,y*tamanhoPixel+tamanhoPixel/2+6*tamanhoPixel+offset);

		}
};

void criarPixel(short x, short y, unsigned char r,unsigned char g,unsigned char b, vector<Ponto> &pixels){
	pixels.push_back(Ponto(x,y,r,g,b));
}

